using System;
using static System.Console;

namespace Bme121
{
    class Complex
    {
		public double x;
		public double y;
		
		public Complex ()
		{
			this.x=0.0;
			this.y=0.0;
		}
		public Complex (double re, double im)
		{
			this.x=re;
			this.y=im;
		}
	}
    static class Program
    {
		
        static void Main()
        {   
            Complex first = new Complex ();
            WriteLine($"first: {first.x} , {first.y}");
            
        }
    }
}
